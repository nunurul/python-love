# konversi suhu

# celcius 

celcius = float(input("Masukan Suhu : "))
print("suhu celcius nya adalah", celcius, "celcius")

# reamur 

reamur = 4/5 * celcius
print("suhu reamur nya adalah", reamur, "reamur")

# fahrenheit

fahrenheit = 9/5 * celcius + 32
print("suhu fahrenheitnya adalah", fahrenheit, "fahrenheit")

# kelvin

kelvin = celcius + 273 
print("suhu kelvinnya adalah", kelvin, "kelvin")